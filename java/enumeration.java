public enum enumuration {
	bucky("nice", "22"),
	kelsey("nice", "222"),
	julia("nice", "2222");
	
	private final String desc;
	private final String year;
	
	enumuration(String description, String birthday){
		desc = description;
		year = birthday;
	}	
	public String getDesc() {
		return desc;
	}
	public String getYear() {
		return year;	
	}
}
